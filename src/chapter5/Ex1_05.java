package chapter5;

public class Ex1_05 {
	public static void main(String[] args) {
		int num = 66;
		
		if (num >= 50) {
			System.out.println("50 이상의 값이 들어있습니다");
		} else {
			System.out.println("50 미만의 값이 들어있습니다");
		}
		
		double height = 160.6;
		
		if (height >= 150.0) {
			System.out.println("탑승 가능");
		} else {
			System.out.println("탑승 불가능");
		}
		
	}
}
